function enableFields(form) {
  form.setEnabled('dataCriacao', false);
  form.setEnabled('id_criador', false);
  form.setEnabled('criador', false);

  form.setEnabled('dataEdicao', false);
  form.setEnabled('id_editor', false);
  form.setEnabled('editor', false);

  form.setEnabled('codigo', false);
}
